import UIKit

extension UIColor {
    class func insuranceGray() -> UIColor {
        return UIColor(red: 239/255, green: 239/255, blue: 244/255, alpha: 1.0)
    }
    
    class func insuranceBlue() -> UIColor {
        return UIColor(red: 38/255, green: 99/255, blue: 163/255, alpha: 1.0)
    }
    
    class func insuranceGreen() -> UIColor {
        return UIColor(red: 199/255, green: 218/255, blue: 68/255, alpha: 1.0)
    }
}
